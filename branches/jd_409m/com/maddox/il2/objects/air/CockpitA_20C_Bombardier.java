    this.jdField_mesh_of_type_ComMaddoxIl2EngineHierMesh.chunkSetAngles("Z_TurnBank2", 0.0F, cvt(this.jdField_fm_of_type_ComMaddoxIl2FmFlightModel.getAOS(), -8.0F, 8.0F, -12.0F, 12.0F), 0.0F);

